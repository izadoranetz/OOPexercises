/* 3) Escreva um algoritmo para imprimir os 10 primeiros números inteiros menores que 0. */

public class Questao03 {
    public static void main(String[] args){

        for(int i = -1; i >= -10; i--){
            System.out.println("O número é " + i);
        }
    }
}
